    public function getAllUser(){

        $sql = "SELECT `id`, `username`, `password`, `email` FROM `users`";

        $stmt = $this->db->query($sql);
    if (is_object($stmt)) {
        $users = $stmt->fetchAll(PDO::FETCH_ASSOC);
    }
      
       return $users;
    }








    

